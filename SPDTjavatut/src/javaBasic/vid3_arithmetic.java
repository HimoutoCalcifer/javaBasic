package javaBasic;

public class vid3_arithmetic {

	public static void main(String[] args) {
		
		
		int x = 20;
		int y = 8;
		System.out.println((x/y) + " remainder: " + (x%y));
		
		
		float a = 20;
		float b = 8;
		float c = a/b;
		System.out.println(c);
		c++;
		System.out.println(c);
		

	}

}
